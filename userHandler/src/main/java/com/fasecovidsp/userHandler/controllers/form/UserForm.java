package com.fasecovidsp.userHandler.controllers.form;

public class UserForm {
    private String name;
    private String cel;
    private String municipioText;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCel() {
        return cel;
    }

    public void setCel(String cel) {
        this.cel = cel;
    }

    public String getMunicipioText() {
        return municipioText;
    }

    public void setMunicipioText(String municipioText) {
        this.municipioText = municipioText;
    }
}
